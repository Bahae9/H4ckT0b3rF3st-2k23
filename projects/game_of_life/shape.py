def draw_shape(x, y, size):
    l = [[x,y]]
    for j in range(1, 4):
        for i in range(size):
            l.append([x+i,y-j])
    
    while size > 1:
        for i in range(0,size):
            l.append([x+i,y])
        size -=2
        y += 1
        x += 1
    

        
    return l

